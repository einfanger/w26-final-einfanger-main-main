public class Song
{
    /// <summary>
    /// Title or name of the song
    /// </summary>
    public string Title { get; set; }
    
    /// <summary>
    /// Length of the song in seconds
    /// </summary>
    public double Length { get; set; }
    
    /// <summary>
    /// Song artist
    /// </summary>
    public string Artist { get; set; }
}